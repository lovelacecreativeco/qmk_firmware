bool oled_task_kb(void) {
    if (!oled_task_user()) { return false; }
    render_32x32_logo(get_highest_layer(layer_state));
    oled_set_cursor(7, 0);
    oled_write_P(PSTR("POCKET EDITOR"), false);

    oled_set_cursor(7, 1);
    oled_write_P(PSTR("Layer: "), false);

    /*
    To change LAYER NAMES displayed on the OLED ensure they are a total of 6 characters, including spaces.
    Example:
    ---------------------------------------------------
    case 0:
          oled_write_P(PSTR("Base  "), false);
          break;
    ---------------------------------------------------
    "B A S E *SPACE* *SPACE*" = 6 characters
    This would change layer 0 to Base on the OLED
    */

    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_P(PSTR("CULL   "), false);
            break;
        case 1:
            oled_write_P(PSTR("EXP/TMP"), false);
            break;
        case 2:
            oled_write_P(PSTR("TONE   "), false);
            break;
        case 3:
            oled_write_P(PSTR("WNDOWS "), false);
            break;

        default:
            oled_write_P(PSTR("N/A   "), false);
    }
    oled_set_cursor(7, 2);
    oled_write_P(PSTR("Status: "), false);
    oled_set_cursor(7, 3);
    led_t led_state = host_keyboard_led_state();
    oled_write_P(led_state.num_lock ? PSTR("NUM ") : PSTR("   "), false);
    oled_write_P(led_state.caps_lock ? PSTR("CAP ") : PSTR("   "), false);
    oled_write_P(led_state.scroll_lock ? PSTR("SCR ") : PSTR("   "), false);

    return false;
}

